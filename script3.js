// AND  - && 

// True   &&  True  --- True
// False  &&  True  --- False
// True   &&  False --- False
// False  &&  False --- False 

console.log(7 === 7  && 6 !== '6')
console.log(4 != 5   && 5 == '5')
console.log(6 !== 6  && 8 === 8)
console.log(6 === 6  && 8 != '8')
console.log(6 ==='6' && 8 !== 8)

// OR  -- ||

// True   ||  True  --- True
// False  ||  True  --- True
// True   ||  False --- True
// False  ||  False --- False 

console.log(7 != 8 || 7 == '7')
console.log(7 != 7 || 7 == '7')
console.log(7 == 7 || 7 === '7')
console.log(7 != 7 || 7 === '7')

// Not
console.log(! 9 == 9)
console.log(! 8 != 8)

